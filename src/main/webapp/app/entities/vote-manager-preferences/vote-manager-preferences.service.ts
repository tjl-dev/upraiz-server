import axios from 'axios';

import { IVoteManagerPreferences } from '@/shared/model/vote-manager-preferences.model';

const baseApiUrl = 'api/vote-manager-preferences';

export default class VoteManagerPreferencesService {
  public find(id: number): Promise<IVoteManagerPreferences> {
    return new Promise<IVoteManagerPreferences>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IVoteManagerPreferences): Promise<IVoteManagerPreferences> {
    return new Promise<IVoteManagerPreferences>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IVoteManagerPreferences): Promise<IVoteManagerPreferences> {
    return new Promise<IVoteManagerPreferences>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IVoteManagerPreferences): Promise<IVoteManagerPreferences> {
    return new Promise<IVoteManagerPreferences>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
